package com.github.wolfie.desktopnotifications;

public class ResourceConversionException extends Exception {
  private static final long serialVersionUID = -8784917388492464893L;

  public ResourceConversionException() {
    super();
  }

  public ResourceConversionException(final String arg0, final Throwable arg1) {
    super(arg0, arg1);
  }

  public ResourceConversionException(final String arg0) {
    super(arg0);
  }

  public ResourceConversionException(final Throwable arg0) {
    super(arg0);
  }

}
